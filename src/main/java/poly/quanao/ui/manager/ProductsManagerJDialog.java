    @Override
    public void delete() {
    DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();

    if (model.getRowCount() == 0) {
        XDialog.alert("Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o ƒë·ªÉ x√≥a!");
        return;
    }

    boolean hasSelection = false;
    for (int i = 0; i < model.getRowCount(); i++) {
        Boolean checked = (Boolean) model.getValueAt(i, 7); // C·ªôt checkbox
        if (Boolean.TRUE.equals(checked)) {
            hasSelection = true;
            break;
        }
    }

    if (!hasSelection) {
        XDialog.alert("B·∫°n ch∆∞a ch·ªçn s·∫£n ph·∫©m n√†o ƒë·ªÉ x√≥a!");
        return;
    }

    if (XDialog.confirm("B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a c√°c s·∫£n ph·∫©m ƒë√£ ch·ªçn?")) {
        try {
            for (int i = model.getRowCount() - 1; i >= 0; i--) {
                Boolean checked = (Boolean) model.getValueAt(i, 7);
                if (Boolean.TRUE.equals(checked)) {
                    int productId = Integer.parseInt(model.getValueAt(i, 0).toString());
                    dao.deleteById(productId); // g·ªçi DAO x√≥a theo ID
                    model.removeRow(i);        // x√≥a kh·ªèi b·∫£ng
                }
            }

            this.clear();
            XDialog.alert("ƒê√£ x√≥a th√†nh c√¥ng c√°c s·∫£n ph·∫©m ƒë∆∞·ª£c ch·ªçn!");
        } catch (Exception e) {
            XDialog.alert("L·ªói khi x√≥a s·∫£n ph·∫©m: " + e.getMessage());
        }
    }
}



