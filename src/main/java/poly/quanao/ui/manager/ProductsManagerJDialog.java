    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblProducts.getRowCount()) {
            this.moveFirst();
        } else {
            tblProducts.clearSelection();
            tblProducts.setRowSelectionInterval(index, index);
            this.edit();
        }    
    }

